<script lang="ts">
    import { afterNavigate, beforeNavigate } from '$app/navigation'
    import Header from '$components/Header.svelte'
    import { page } from '$app/stores'
    import { getBgOpacity } from './+layout'
    import { isMenuOpen } from '$lib/stores'
    import { MetaTags } from 'svelte-meta-tags'

    import {
        BASE_URL,
        DEFAULT_DESCRIPTION,
        DEFAULT_OG_IMAGE,
        DEFAULT_OG_IMAGE_SQUARE,
        TAGLINE,
    } from '$lib/constants'

    import '../app.css'
    import CommunitySection from '$components/CommunitySection.svelte'
    import StoriesSection from '$components/StoriesSection.svelte'

    export const csr = false
    export const prerender = true

    $: bodyStyle = `
<style>
    body, .filters-backdrop {
        background: linear-gradient(0deg, #000, rgb(0 0 0 / ${getBgOpacity($page?.route?.id)}%));
    }
</style>
`

    // Workaround to allow smooth scrolling in Firefox
    // https://github.com/sveltejs/kit/issues/2733#issuecomment-1050779671
    beforeNavigate(({ to, from }) => {
        if (to?.url?.pathname !== from?.url?.pathname) {
            document.documentElement.style.scrollBehavior = 'auto'
        }
    })
    afterNavigate(() => (document.documentElement.style.scrollBehavior = ''))
</script>

<!-- titleTemplate={`%s | ${TAGLINE}`} -->
<MetaTags
    title={TAGLINE}
    description={DEFAULT_DESCRIPTION}
    canonical={BASE_URL}
    openGraph={{
        url: BASE_URL,
        title: TAGLINE,
        description: DEFAULT_DESCRIPTION,
        images: [
            {
                url: DEFAULT_OG_IMAGE,
                width: 1200,
                height: 736,
                alt: 'Inner Development Goals Community logo',
            },
            {
                url: DEFAULT_OG_IMAGE_SQUARE,
                width: 1000,
                height: 1000,
                alt: 'Inner Development Goals Community logo',
            },
        ],
    }}
/>

<svelte:head>
    {@html bodyStyle}
</svelte:head>

<main class="mx-auto w-full max-w-2xl px-4 pb-16 text-stone-50 sm:max-w-6xl sm:text-lg">
    <Header />
    <div class:hidden={$isMenuOpen}>
        <slot />
    </div>
</main>

<div class="mx-auto w-full max-w-2xl px-4 pb-16 sm:max-w-3xl" class:hidden={$isMenuOpen}>
    {#if $page?.route?.id === '/'}
        <CommunitySection />
    {:else if $page?.route?.id === '/stories/[link]'}
        <StoriesSection />
    {/if}
</div>
